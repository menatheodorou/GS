-(UIImage *)standardImageFromCurrentScale:(float)scale {
    return [self standardImageFromCurrentScale:scale additionalRotation:0.0f];
}

-(UIImage *)standardImageFromCurrentScale:(float)scale additionalRotation:(CGFloat)degrees {
    
    CGSize imageSize = [self size];
    // TODO: For some reason, there's an orientation translation happening that we need to invert these values.
    // I need to figure out why this is.
    UIDeviceOrientation deviceOrientation = [[UIDevice currentDevice] orientation];
    CGFloat standardWidth = imageSize.height;
    CGFloat standardHeight = imageSize.width;
    if (!UIDeviceOrientationIsPortrait(deviceOrientation)) {
        standardWidth = imageSize.width;
        standardHeight = imageSize.height;
    }
    CGFloat scaleFactor = 1 / scale;
    CGFloat scaledWidth = standardWidth * scaleFactor;
    CGFloat scaledHeight = standardHeight * scaleFactor;
    CGRect standardImageBounds = CGRectMake((standardWidth / 2) - (scaledWidth / 2), (standardHeight / 2) - (scaledHeight / 2), scaledWidth, scaledHeight);

    
    
    CGImageRef newImage = CGImageCreateWithImageInRect([self CGImage], standardImageBounds);
    UIImage *croppedImage = [UIImage imageWithCGImage:newImage];
    
    switch (deviceOrientation) {
        case UIDeviceOrientationPortrait:
            croppedImage = [croppedImage imageRotatedByDegrees:90.0 + degrees];
            break;
        case UIDeviceOrientationPortraitUpsideDown:
            croppedImage = [croppedImage imageRotatedByDegrees:-90.0 + degrees];
            break;
        case UIDeviceOrientationLandscapeLeft:
            croppedImage = [croppedImage imageRotatedByDegrees:0 + degrees];
            break;
        case UIDeviceOrientationLandscapeRight:
            croppedImage = [croppedImage imageRotatedByDegrees:180 + degrees];
            break;
        default:

            break;
    }
    
    CFRelease(newImage);
    UIImage* img= [croppedImage imageByScalingProportionallyToSize:imageSize];
    
    NSLog(@"%@",NSStringFromCGSize(img.size));
    return img;
    
}

CGFloat DegreesToRadians(CGFloat degrees) {return degrees * M_PI / 180;};
CGFloat RadiansToDegrees(CGFloat radians) {return radians * 180/M_PI;};

-(UIImage *)imageAtRect:(CGRect)rect
{

CGImageRef imageRef = CGImageCreateWithImageInRect([self CGImage], rect);
UIImage* subImage = [UIImage imageWithCGImage: imageRef scale:self.scale orientation:self.imageOrientation];
CGImageRelease(imageRef);

return subImage;

}

- (UIImage *)imageByScalingProportionallyToMinimumSize:(CGSize)targetSize {
    
    UIImage *sourceImage = self;
    UIImage *newImage = nil;
    
    CGSize imageSize = sourceImage.size;
    CGFloat width = imageSize.width;
    CGFloat height = imageSize.height;
    
    CGFloat targetWidth = targetSize.width;
    CGFloat targetHeight = targetSize.height;
    
    CGFloat scaleFactor = 0.0;
    CGFloat scaledWidth = targetWidth;
    CGFloat scaledHeight = targetHeight;
    
    CGPoint thumbnailPoint = CGPointMake(0.0,0.0);
    
    if (CGSizeEqualToSize(imageSize, targetSize) == NO) {
        
        CGFloat widthFactor = targetWidth / width;
        CGFloat heightFactor = targetHeight / height;
        
        if (widthFactor > heightFactor) 
            scaleFactor = widthFactor;
        else
            scaleFactor = heightFactor;
        
        scaledWidth  = width * scaleFactor;
        scaledHeight = height * scaleFactor;
        
        // center the image
        
        if (widthFactor > heightFactor) {
            thumbnailPoint.y = (targetHeight - scaledHeight) * 0.5; 
        } else if (widthFactor < heightFactor) {
            thumbnailPoint.x = (targetWidth - scaledWidth) * 0.5;
        }
    }
    
    
    // this is actually the interesting part:
    
    UIGraphicsBeginImageContext(targetSize);
    
    CGRect thumbnailRect = CGRectZero;
    thumbnailRect.origin = thumbnailPoint;
    thumbnailRect.size.width  = scaledWidth;
    thumbnailRect.size.height = scaledHeight;
    
    [sourceImage drawInRect:thumbnailRect];
    
    newImage = UIGraphicsGetImageFromCurrentImageContext();
    UIGraphicsEndImageContext();
    
    if(newImage == nil) NSLog(@"could not scale image");
    
    
    return newImage ;
}


